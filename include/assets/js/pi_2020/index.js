$('.mpTab').on('click', (env) => {
	$('.mpr-tab-contend').html('Loading...');
	$('.mpTab').removeClass('mpTab-active');
	// $.each($('.mpTab>a>img'), (env) => {
	// });
	Array.from(document.querySelectorAll('.mpTab>a>img')).forEach(env => {
		const ele = $(env);
		const img = `${baseURL}/include/assets/img/pi_2020/filterIcons/${ele.data('imgname')}-default.svg`;
		ele.prop('src', img);
	});
	ele = $(env.target);
	if (ele.hasClass('mpTab')) {
		$(env.target).addClass('mpTab-active');
	} else {
		ele.closest('.mpTab').addClass('mpTab-active');
	}
	selectedTab = $('.mpTab-active');

	const img = selectedTab.find('img');
	const cardImag = img.data('imgname');
	img.prop('src', `${baseURL}/include/assets/img/pi_2020/filterIcons/${cardImag}-selected.svg`)

	if (selectedTab.prop('id') == 'hybridvarietyreleased') {
		hybridVarietyReleased.init();
	} else if (selectedTab.prop('id') == 'seedproduced') {
		seedProduced.init();
	} else if (selectedTab.prop('id') == 'digitalinitiative') {
		digitalInitatives.init();
	} else if (selectedTab.prop('id') == 'capacitybuilding') {
		capacityBuilding.init();
	} else if (selectedTab.prop('id') == 'watershed') {
		watershed.init();
	} else if (selectedTab.prop('id') == 'nationalTrails') {
		hybridNationalTrails.init();
	} else if (selectedTab.prop('id') == 'soilmanagement') {
		soilManagement.init(); // mar-1
	} else if (selectedTab.prop('id') == 'learnerparticipants') {
		learnerParticipants.init();
	} else if (selectedTab.prop('id') == 'publicationinoar') {
		publicationInOar.init();
	} else if (selectedTab.prop('id') == 'granttab') {
		grant.init();
	}
});

